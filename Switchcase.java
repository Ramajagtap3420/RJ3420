import java.util.*;
class Switchcase
{
	public static void main(String args[])
	{
	int day=2;
		
	switch(day)
	{
	case 1:
	System.out.println("im in leave");
	break;
	case 2:
	System.out.println("im in office");
	break;
	case 3:
	System.out.println("im in school");
	break;
	case 4:
	System.out.println("im in home");
	break;

	

	}
	
	}
}
